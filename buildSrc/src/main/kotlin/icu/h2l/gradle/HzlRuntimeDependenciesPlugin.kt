package icu.h2l.gradle

import java.security.MessageDigest
import java.util.Base64
import java.util.Properties
import org.gradle.api.Plugin
import org.gradle.api.Project
import org.gradle.api.artifacts.Configuration
import org.gradle.api.artifacts.ResolvedArtifact
import org.gradle.api.attributes.Attribute
import org.gradle.api.tasks.Copy
import org.gradle.kotlin.dsl.named
import org.gradle.kotlin.dsl.register

class HzlRuntimeDependenciesPlugin : Plugin<Project> {
    override fun apply(project: Project) {
        val needPackage = project.configurations.maybeCreate("needPackage").apply {
            isCanBeConsumed = false
            isCanBeResolved = false
            description = "Runtime libraries that should be downloaded and injected by HyperZoneLogin at startup."
        }

        val needPackageResolver = project.configurations.maybeCreate("needPackageResolver").apply {
            isCanBeConsumed = false
            isCanBeResolved = true
            extendsFrom(needPackage)
        }

        project.plugins.withId("org.jetbrains.kotlin.jvm") {
            configureMetadataGeneration(project, needPackageResolver)
        }
    }

    private fun configureMetadataGeneration(project: Project, resolver: Configuration) {
        val runtimeClasspath = project.configurations.named("runtimeClasspath").get()
        copyAttributes(runtimeClasspath, resolver)

        val outputFile = project.layout.buildDirectory.file(
            "generated/hzl/runtime-dependencies/runtime-dependencies.properties"
        )

        val generateTask = project.tasks.register("generateRuntimeDependencyMetadata") {
            inputs.files(resolver)
            outputs.file(outputFile)

            doLast {
                val artifacts = resolver.resolvedConfiguration.resolvedArtifacts
                    .filter(::isExternalJar)
                    .sortedBy { artifactId(it) }

                val properties = Properties().apply {
                    setProperty("count", artifacts.size.toString())
                }

                artifacts.forEachIndexed { index, artifact ->
                    val moduleId = artifact.moduleVersion.id
                    properties.setProperty("dependency.$index.group", moduleId.group)
                    properties.setProperty("dependency.$index.name", artifact.name)
                    properties.setProperty("dependency.$index.version", moduleId.version)
                    properties.setProperty("dependency.$index.sha256", sha256(artifact))
                }

                val file = outputFile.get().asFile
                file.parentFile.mkdirs()
                file.outputStream().use { out ->
                    properties.store(out, "Generated by icu.h2l.runtime-dependencies")
                }
            }
        }

        project.tasks.named<Copy>("processResources") {
            from(generateTask) {
                into("META-INF/hzl")
                rename { "runtime-dependencies.properties" }
            }
        }
    }

    private fun copyAttributes(source: Configuration, target: Configuration) {
        source.attributes.keySet().forEach { key ->
            @Suppress("UNCHECKED_CAST")
            val typedKey = key as Attribute<Any>
            val value = source.attributes.getAttribute(typedKey) ?: return@forEach
            target.attributes.attribute(typedKey, value)
        }
    }

    private fun isExternalJar(artifact: ResolvedArtifact): Boolean {
        val moduleId = artifact.moduleVersion.id
        return artifact.extension == "jar" && moduleId.group != "unspecified"
    }

    private fun artifactId(artifact: ResolvedArtifact): String {
        val moduleId = artifact.moduleVersion.id
        return "${moduleId.group}:${artifact.name}:${moduleId.version}"
    }

    private fun sha256(artifact: ResolvedArtifact): String {
        val digest = MessageDigest.getInstance("SHA-256")
        artifact.file.inputStream().use { input ->
            val buffer = ByteArray(DEFAULT_BUFFER_SIZE)
            while (true) {
                val read = input.read(buffer)
                if (read == -1) {
                    break
                }
                digest.update(buffer, 0, read)
            }
        }
        return Base64.getEncoder().encodeToString(digest.digest())
    }
}

